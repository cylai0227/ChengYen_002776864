    public void populateTable()
    {
        DefaultTableModel dtm = (DefaultTableModel)tblMedicine.getModel();
        dtm.setRowCount(0);
        
        
        for(Medicine m : mcg.getMedicineList())
        {
                Object row[] = new Object[2];
                row[0] = m.getMedicineName();
                row[1] = m.getDosage();
                dtm.addRow(row);
        }
    }
    
    
    
    
    
