    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
          if(txtDosage.getText().isEmpty()||txtmName.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Don't accept empty value");
        } 



        String mname = txtmName.getText();
        double dosage = Integer.parseInt(txtDosage.getText());
        
        
        Medicine m = mcg.addMedicine();
        m.setMedicineName(mname);
        m.setDosage(dosage);
        
        JOptionPane.showMessageDialog(null, "Medicine Added");
        
        txtDosage.setText("");
        txtmName.setText("");
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblMedicine.getSelectedRow();

        if(selectedrow >=0)
        {
            Medicine mdc = (Medicine)tblMedicine.getValueAt(selectedrow,0);
            mcg.deleteMedicine(mdc);
            JOptionPane.showMessageDialog(null,"Vital sign has been deleted");
         
        }
        else
        JOptionPane.showMessageDialog(null, "please select any row");

    }//GEN-LAST:event_btnDeleteActionPerformed


